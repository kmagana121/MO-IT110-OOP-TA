    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
       
        
        int salary = Integer.parseInt(txt_BasicSal.getText());

     
            if(r_percentage.isSelected() == true){
        
        int getPercentage = Integer.parseInt(txt_opt1.getText());
        int calcPercentage = salary/100*getPercentage + salary;
        String P = String.valueOf(calcPercentage);
        txt_BasicSal.setText(P);
        
        }else if (r_amount.isSelected() == true){
       
       int getAmount = Integer.parseInt (text_opt2.getText());
       int calAmount = salary +getAmount;
       String A = String.valueOf(calAmount);
       txt_BasicSal.setText(A);
        }

        try{
            String value1 = txt_empID.getText();
            String value2 = txt_BasicSal.getText();
            
            String sql = "update Employee_info set id = '"+value1+"', BasicSal = '"+value2+"' where id = '"+value1+"' ";
                    
            pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Record Updated!");
            
}catch (Exception e){
    JOptionPane.showMessageDialog(null, e);
}
finally{
            try{
                rs.close();
                pst.close();
            
}catch (Exception e){
            }
        }


        
        
    }//GEN-LAST:event_jButton1ActionPerformed

