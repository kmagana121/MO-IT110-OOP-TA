    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // TODO add your handling code here:
        
        
        try{
            String sql = "select * from Employee_info where id = ?";
            
            pst = conn.prepareStatement (sql);
            pst.setString (1, txt_search.getText());
            rs = pst.executeQuery();
            
            
            String add1 = rs.getString ("ID");
            txt_empID.setText(add1);
            
            String add2 = rs.getString ("Surname");
            txt_Surname.setText(add2);
            
            String add3 = rs.getString ("First_Name");
            txt_FirstName.setText(add3);
            
            String add4= rs.getString ("Imm_Spr");
            txt_ImmSpr.setText(add4);
            
            String add5 = rs.getString ("Department");
            txt_Dept.setText(add5);
            
            String add6 = rs.getString ("BasicSal");
            txt_BasicSal.setText(add6);
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
            
        }finally{
            
            try{
                rs.close();
                pst.close();
            
            
}catch (Exception e){
        }
        
        
        
        }
        
        
    }//GEN-LAST:event_txt_searchKeyReleased

